    private void categoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_categoryActionPerformed
        // TODO add your handling code here:
        try{
            items.addItem("SELECT");
            ResultSet rs;
            Class.forName("oracle.jdbc.driver.OracleDriver");
            Connection conn=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","system","avinash");
            String sql="select * from items where category_name=?";
            PreparedStatement ps=conn.prepareStatement(sql);
            ps.setString(1,category.getSelectedItem().toString());
            rs=ps.executeQuery();
            while(rs.next()){
                items.addItem(rs.getString("item_name"));
            }
            if(!(category.getSelectedItem().equals("SELECT")))
                 items.addItem("OTHERS");
        }
        catch(ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_categoryActionPerformed

