    private void addAmountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAmountActionPerformed
        // TODO add your handling code here:
        if(category.getSelectedItem().equals("SELECT"))
        {
            JOptionPane.showMessageDialog(null, "Plese Select Category ","Error",JOptionPane.ERROR_MESSAGE);
        }
        else if(items.getSelectedItem().equals("SELECT"))
        {
            JOptionPane.showMessageDialog(null, "Plese Select the Items you Buys ","Error",JOptionPane.ERROR_MESSAGE);
        }
        else if(items.getSelectedItem().equals("OTHERS"))
        {
            JOptionPane.showMessageDialog(null, "Plese Enter the Name of Items you Buys and Add it","Error",JOptionPane.ERROR_MESSAGE);
        }
        else if(amount.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Plese Enter the Price of Items you Buys ","Error",JOptionPane.ERROR_MESSAGE);
        }
        else
        {   
            Amount=Double.parseDouble(totalAmount.getText())+Double.parseDouble(amount.getText());
            totalAmount.setText(String.valueOf(Amount));
            bills.setText(bills.getText()+items.getSelectedItem().toString()+"  -  Rs"+amount.getText()+", \n");
            items.setSelectedItem("SELECT");
            amount.setText("");
        }
        
        
    }//GEN-LAST:event_addAmountActionPerformed

    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        java.util.Date currentDate=cal.getTime();
        if(!(bills.getText().equals("")))
        {
        try{
            Class.forName("oracle.jdbc.driver.OracleDriver");
            Connection conn=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","system","avinash");
            PreparedStatement ps=conn.prepareStatement("insert into income values(?,?,?,?,?,?)");
            ps.setDate(1,new java.sql.Date(currentDate.getTime()));
            ps.setString(2,Id);
            ps.setString(3,Name);
            ps.setString(4,String.valueOf(bills.getText()));
            ps.setString(5, "EXPENSE");
            ps.setString(6,String.valueOf(totalAmount.getText()));
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Expense Data Submitted Successfully","Success",JOptionPane.INFORMATION_MESSAGE);
            Choice choice=new Choice();
            choice.Id=Id;
            choice.idlabel.setText(Id);
            choice.Name=Name;
            choice.welcometext.setText(Name);
            conn.close();
            ps.close();
            switch (UserType) {
                case "Admin":
                    choice.UserType="Admin";
                    break;
                case "Manager":
                    choice.UserType="Manager";
                    break;
                default:
                    choice.UserType="User";
                    break;
            }
            choice.setVisible(true);
            this.setVisible(false);
        }
        catch(HeadlessException | ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Select and Fill the Details Completely then Submit Again","ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_submitActionPerformed

